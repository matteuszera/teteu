def calcular_area_retangulo(comprimento, largura):  #Funçao que calcula a area de um retangulo
    area = comprimento * largura
    return area

area_retangulo = calcular_area_retangulo(5, 4)
print(f"A área do retângulo é {area_retangulo} unidades quadradas") #Chamada da funçao e armazenamento do resultado em uma variavel
